/**
 * Tileset definitions for Gearbreakers.
 * Each tileset is a canvas containing a 16-column grid of 16x16 tiles.
 * Tile IDs: 0=floor, 1=wall, 2=interactable, 3=transition, 4=hazard, 5-7=variants.
 */
import { drawPixelArt } from './spriteGenerator.js';

// ─── Helpers ───

/** Assemble an array of 16x16 tile canvases into a single tileset canvas (16 columns). */
function assembleTileset(tiles) {
  const cols = 16;
  const rows = Math.ceil(tiles.length / cols);
  const canvas = document.createElement('canvas');
  canvas.width = cols * 16;
  canvas.height = rows * 16;
  const ctx = canvas.getContext('2d');
  for (let i = 0; i < tiles.length; i++) {
    const x = (i % cols) * 16;
    const y = Math.floor(i / cols) * 16;
    ctx.drawImage(tiles[i], x, y);
  }
  return canvas;
}

