// ─── MAIN EXPORT ───

export function generateUISprites() {
  // Item icons: 8 icons × 16px = 128×16
  const itemIcons = createSpritesheetH([
    makeHealthStim(),
    makeMegaStim(),
    makeChargeCell(),
    makeMegaCell(),
    makeAntidoteKit(),
    makeReviveCoil(),
    makeSmokeBomb(),
    makeAetherShard()
  ]);

  // Status icons: 6 icons × 8px = 48×8
  const statusIcons = createSpritesheetH([
    makeBurnIcon(),
    makeStunIcon(),
    makeCorrodeIcon(),
    makeBlindIcon(),
    makeOverclockIcon(),
    makeShieldIcon()
  ]);

  return {
    ui_item_icons: itemIcons,
    ui_status_icons: statusIcons
  };
}
