// ─── Shared walk sheet builder ───

/**
 * Build a 48x64 walk spritesheet from frame data.
 * Grid layout: 3 cols (left-step, neutral, right-step) x 4 rows (down, left, right, up).
 * Left-facing frames are generated by mirroring right-facing frames.
 */
function buildWalkSheet(frames, palette) {
  const {
    downNeutral, downLeft, downRight,
    upNeutral, upLeft, upRight,
    rightNeutral, rightLeft, rightRight
  } = frames;

  // Render right-facing canvases
  const rNeutral = drawPixelArt(16, 16, rightNeutral, palette);
  const rLeft    = drawPixelArt(16, 16, rightLeft, palette);
  const rRight   = drawPixelArt(16, 16, rightRight, palette);

  // Mirror for left-facing
  const lNeutral = mirrorHorizontal(rNeutral);
  const lLeft    = mirrorHorizontal(rRight); // mirrored right-step becomes left-step
  const lRight   = mirrorHorizontal(rLeft);  // mirrored left-step becomes right-step

  // Build grid: [row][col] where col order is left-step, neutral, right-step
  const grid = [
    // Row 0: Down
    [
      drawPixelArt(16, 16, downLeft, palette),
      drawPixelArt(16, 16, downNeutral, palette),
      drawPixelArt(16, 16, downRight, palette),
    ],
    // Row 1: Left (mirrored from right)
    [lLeft, lNeutral, lRight],
    // Row 2: Right
    [rLeft, rNeutral, rRight],
    // Row 3: Up
    [
      drawPixelArt(16, 16, upLeft, palette),
      drawPixelArt(16, 16, upNeutral, palette),
      drawPixelArt(16, 16, upRight, palette),
    ],
  ];

  return createSpritesheetGrid(grid, 16, 16);
}

